<template>
  <div class="google-signin">
    <button 
      @click="handleSignIn" 
      :disabled="isSigningIn"
      class="google-signin-button"
    >
      <div class="google-icon">
        <svg width="18" height="18" viewBox="0 0 24 24">
          <path fill="#4285f4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
          <path fill="#34a853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
          <path fill="#fbbc05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
          <path fill="#ea4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
        </svg>
      </div>
      <span v-if="!isSigningIn">Sign in with Google</span>
      <span v-else>Signing in...</span>
    </button>
    
    <div v-if="error" class="error-message">
      {{ error }}
    </div>
  </div>
</template>

<script>
import { googleAuthService } from '../../services/googleAuth.js'
import { authService } from '../../services/authService.js'

export default {
  name: 'GoogleSignInButton',
  data() {
    return {
      isSigningIn: false,
      error: null
    }
  },
  methods: {
    async handleSignIn() {
      this.isSigningIn = true
      this.error = null

      try {
        const googleResult = await googleAuthService.signIn()
        
        const authResult = await authService.googleSignIn(googleResult.idToken)
        
        this.$emit('sign-in-success', {
          user: authResult.user,
          expiresAt: authResult.expiresAt
        })

      } catch (error) {
        this.error = error.message
        this.$emit('sign-in-error', error)
      } finally {
        this.isSigningIn = false
      }
    }
  }
}
</script>

